#include "general.h"

/* ------------------------------------------------------------------------ *
 *                                                                          *
 *  _wait( delay )                                               			*
 *                                                                          *
 *      Wait in a software loop for 'x' delay                               *
 *                                                                          *
 * ------------------------------------------------------------------------ */
void _wait( unsigned int delay )
{
    volatile unsigned int i = delay;

    while(i--);
}



